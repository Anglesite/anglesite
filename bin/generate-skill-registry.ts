// Generate docs/dev/skill-registry.md from skill frontmatter.
// Scans each skills/<name>/SKILL.md, reads YAML frontmatter, and produces a
// markdown registry grouped by classification (user-facing, model-only, both).
//
// Usage: npx tsx bin/generate-skill-registry.ts

import { readdirSync, readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { join, resolve } from "node:path";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface SkillMeta {
  name: string;
  description: string;
  disableModelInvocation?: boolean;
  userInvokable?: boolean;
}

export type SkillClassification = "user-facing" | "model-only" | "both";

// ---------------------------------------------------------------------------
// parseSkillFrontmatter
// ---------------------------------------------------------------------------

export function parseSkillFrontmatter(content: string): SkillMeta {
  const fmMatch = content.match(/^---\n([\s\S]*?)\n---/);
  const fm = fmMatch ? fmMatch[1] : "";

  const name = fm.match(/^name:\s*(.+)$/m)?.[1]?.trim().replace(/^["']|["']$/g, "") ?? "";
  const description =
    fm.match(/^description:\s*(.+)$/m)?.[1]?.trim().replace(/^["']|["']$/g, "") ?? "";

  const meta: SkillMeta = { name, description };

  if (/^disable-model-invocation:\s*true$/m.test(fm)) {
    meta.disableModelInvocation = true;
  }

  if (/^user-invokable:\s*false$/m.test(fm)) {
    meta.userInvokable = false;
  }

  return meta;
}

// ---------------------------------------------------------------------------
// classifySkill
// ---------------------------------------------------------------------------

export function classifySkill(meta: SkillMeta): SkillClassification {
  if (meta.userInvokable === false) return "model-only";
  if (meta.disableModelInvocation === true) return "user-facing";
  return "both";
}

// ---------------------------------------------------------------------------
// generateRegistry — produce markdown from skill metadata
// ---------------------------------------------------------------------------

export function generateRegistry(skills: SkillMeta[]): string {
  const userFacing = skills
    .filter((s) => classifySkill(s) === "user-facing")
    .sort((a, b) => a.name.localeCompare(b.name));
  const modelOnly = skills
    .filter((s) => classifySkill(s) === "model-only")
    .sort((a, b) => a.name.localeCompare(b.name));
  const both = skills
    .filter((s) => classifySkill(s) === "both")
    .sort((a, b) => a.name.localeCompare(b.name));

  const lines: string[] = [
    "<!-- Auto-generated by bin/generate-skill-registry.ts — do not edit manually -->",
    "",
    `# Skill Registry (${skills.length} skills)`,
    "",
  ];

  function addSection(
    title: string,
    subtitle: string,
    items: SkillMeta[],
  ) {
    if (items.length === 0) return;
    lines.push(`## ${title}`, "");
    lines.push(subtitle, "");
    lines.push("| Skill | Purpose |", "|---|---|");
    for (const s of items) {
      lines.push(`| \`${s.name}\` | ${s.description} |`);
    }
    lines.push("");
  }

  addSection(
    "User-facing",
    `Invoked via \`/anglesite:<name>\` (${userFacing.length} skills):`,
    userFacing,
  );

  addSection(
    "Model-only",
    `Called programmatically by other skills (${modelOnly.length} skills):`,
    modelOnly,
  );

  addSection(
    "Both",
    `Can be invoked by users or called by other skills (${both.length} skills):`,
    both,
  );

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// scanSkills — read all skills from disk
// ---------------------------------------------------------------------------

export function scanSkills(skillsDir: string): SkillMeta[] {
  const skills: SkillMeta[] = [];

  for (const entry of readdirSync(skillsDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const skillFile = join(skillsDir, entry.name, "SKILL.md");
    if (!existsSync(skillFile)) continue;

    const content = readFileSync(skillFile, "utf-8");
    const meta = parseSkillFrontmatter(content);
    if (meta.name) {
      skills.push(meta);
    }
  }

  return skills;
}

// ---------------------------------------------------------------------------
// Main — only runs when executed directly
// ---------------------------------------------------------------------------

const isMain =
  process.argv[1]?.endsWith("generate-skill-registry.ts") ||
  process.argv[1]?.endsWith("generate-skill-registry.js");

if (isMain) {
  const root = resolve(import.meta.dirname ?? ".", "..");
  const skillsDir = join(root, "skills");
  const outDir = join(root, "docs", "dev");
  const outFile = join(outDir, "skill-registry.md");

  const skills = scanSkills(skillsDir);
  const registry = generateRegistry(skills);

  if (!existsSync(outDir)) {
    mkdirSync(outDir, { recursive: true });
  }

  writeFileSync(outFile, registry + "\n");

  const userFacing = skills.filter((s) => classifySkill(s) === "user-facing").length;
  const modelOnly = skills.filter((s) => classifySkill(s) === "model-only").length;
  const both = skills.filter((s) => classifySkill(s) === "both").length;

  console.log(
    `Wrote ${outFile} (${skills.length} skills: ${userFacing} user-facing, ${modelOnly} model-only, ${both} both)`,
  );
}
