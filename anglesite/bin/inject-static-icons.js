function injectStaticIcons() {
  const templatePath = path.join(__dirname, '..', 'src', 'renderer', 'ui', 'templates', 'website-editor.html');

  if (!fs.existsSync(templatePath)) {
    console.error('Template file not found:', templatePath);
    process.exit(1);
  }

  let content = fs.readFileSync(templatePath, 'utf8');

  // Replace all data-lucide icons with static SVG
  for (const [iconName, svgContent] of Object.entries(icons)) {
    const regex = new RegExp(`<i data-lucide="${iconName}"></i>`, 'g');
    content = content.replace(regex, svgContent);
  }

  // Remove lucide-related JavaScript since we're using static icons
  content = content.replace(
    /if \(typeof lucide !== 'undefined'\) \{\s*lucide\.createIcons\(\);\s*\}/g,
    '// Static icons - no JavaScript needed'
  );
  content = content.replace(/lucide\.createIcons\(\);\s*$/gm, '// Static icons - no JavaScript needed');

  fs.writeFileSync(templatePath, content);
  console.log('âœ… Static icons injected successfully');
}

if (require.main === module) {
  injectStaticIcons();
}

module.exports = { injectStaticIcons };
